package assignment1;
public class ValueUpdation {
	public static void main(String[] args) {
		byte a=10;
		a=20;
		System.out.println("Update value of byte is " +a);
		int a1 = 56565;
		a1 = 656564;
		System.out.println("Update value of int is " +a1);		
		short a2 = 200;
		a2 = 400;
		System.out.println("Update value of short is " +a2);
		long a3 = 88877799;
		a3 = 900991991;
		System.out.println("Update value of long is " +a3);
		
		float f1 = 8692.67788f;
		f1 = 673.47849f;
		System.out.println("Update value of float is " +f1);
		double d1 = 367.78992;
		d1 = 67.5678929;
		System.out.println("Update value of double is " +d1);
		
		char c1 = 'A';
		c1 = 'R';
		System.out.println("Update value of char is " +c1);
		
		String s1 = "Good day";
		s1 = "Nice day";
		System.out.println("Update value of string is " +s1);
		
		boolean b1= true;
		b1 = false;
		System.out.println("Update value of boolean is " +b1);
		}
}
